package com.company;

public class Person {
     String name;
     int ID , age;

    public Person(String name, int ID, int age){
        this.name = name;
        this.ID = ID;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public int getID() {
        return ID;
    }

    public int getAge() {
        return age;
    }
}






